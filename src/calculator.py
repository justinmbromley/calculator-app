class Calculator:
    def plus(num_1: int, num_2: int) -> int:
        return num_1 + num_2
    
    def subtract(num_1: int, num_2: int) -> int:
        return num_1 - num_2
    
    def product(num_1: int, num_2: int) -> int:
        return num_1 * num_2
    
    def quotient(num_1: int, num_2: int) -> int:
        return num_1 / num_2
